void main() { //Fungsi utama program.
  int i = 10; //Deklarasi variable i dengan nilai 10.

  while (i >= 1) { //Perulangan yang akan terus berjalan selama i lebih besar atau sama dengan 1.
    print('*' * i); //Mencetak '*' sesuai dengan perulangan yang terjadi.
    i--; //Setiap iterasi i akan berkurang 1 agar jumlah * yang dicetak pada baris berikutnya berkurang.
  }
}