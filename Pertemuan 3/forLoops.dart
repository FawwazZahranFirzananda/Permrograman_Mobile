void main() { //Function utama program
  for (int i = 1; i <= 10; i++) { //Struktur perulangan yang digunakan untuk iterasi mulai dari 1 hingga 10. Pada setiap iterasi akan bertambah 1.
    print('*' * i); //Mencetak '*' sebanyak 10 kali yang pada setiap iterasi jumlah bintang yang dicetak akan bertambah 1, maka akan membentuk pola segitiga.
  }
}